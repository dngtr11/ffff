    private void showData(List<GiangVien> list) {
        dtm.setRowCount(0);
        for (GiangVien gv : list) {
            dtm.addRow(new Object[]{
                gv.getMaGV(),
                gv.getTenGV(),
                gv.isGioiTinh(),
                gv.getLoai(),
                gv.getNamSinh()
            });
        }
    }

    public GiangVien getFormData() {
        String ten = txtTen.getText();
        String ma = txtMa.getText();
        String namSinh = (String) cbbNamSinh.getSelectedItem();
        String loai = (String) cbbLoai.getSelectedItem();
        boolean gioiTinh = rdNam.isSelected();
        return new GiangVien(ma, ten, Integer.parseInt(namSinh), gioiTinh, loai);

    }

