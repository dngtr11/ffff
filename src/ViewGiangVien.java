    private void showInfo(GiangVien gv) {
        txtMa.setText(gv.getMaGV());
        txtTen.setText(gv.getTenGV());
        cbbLoai.setSelectedItem(gv.getLoai());
        cbbNamSinh.setSelectedItem(gv.getNamSinh());
        if (gv.isGioiTinh() == true) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
    }

    public void getOne() {
        GiangVien one = service.getList().get(0);
        showInfo(one);
    }

