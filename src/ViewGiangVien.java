    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        // TODO add your handling code here:
        int dem = 0;
        String ma = txtMa.getText();
        if (ma.isEmpty()) {
            dem++;
            lblMaError.setText("khong duoc de trong");
        } else {
            try {
                if (!ma.matches("\\d+")) {
                    dem++;
                    lblMaError.setText("ma phai là so duong");
                } else if (chungMa(ma)) {
                    dem++;
                    lblMaError.setText("ma da ton tai ");
                } else {
                    lblMaError.setText(" ");
                }
            } catch (Exception e) {
                dem++;
                lblMaError.setText("loi dinh dang");
            }

        }

        String ten = txtTen.getText();
        if (ten.isEmpty()) {
            dem++;
            lblTenError.setText("ten khong duoc de trong");
        } else {
            lblTenError.setText(" ");
        }
        if (!ten.matches("[A-Za-z]+")) {
            dem++;
            lblTenError.setText("phai la chu");
        } else {
            lblTenError.setText(" ");
        }
        String namSinh = (String) cbbNamSinh.getSelectedItem();
        String loai = (String) cbbLoai.getSelectedItem();
        boolean gioiTinh = rdNam.isSelected();
        if (dem == 0) {
            service.add(getFormData());
            showData(listGV);
            JOptionPane.showMessageDialog(this, "them thanh cong");
        }
    }//GEN-LAST:event_btAddActionPerformed

